// - **Examples**: `String`, `Number`, `Boolean`, `Null`, `Undefined`, `Symbol`, `BigInt`
// - **Characteristics**:
//   - **Immutable**: Cannot be changed.
//   - **Stored in Stack**.
//   - **Copied by Value**.

// ### Non-Primitive Types
// - **Examples**: `Object`, `Array`, `Function`
// - **Characteristics**:
//   - **Mutable**: Can be changed.
//   - **Stored in Heap**.
//   - **Copied by Reference**.